
text = input("введите строку:")
def strr_upper(text):
   """Функция котора принимает на вход
   строку и возвращает ее
   со всеми заглавными буквами."""
   print(text.upper())
def strr_title(text):
   """Функция которая делает заглавными
   первые буквы каждого слова в строке,
    поступившей на вход функции"""
   print(text.title())




strr_upper(text)
strr_title(text)





